import { Schema, model } from "mongoose";

const formSchema = new Schema({
  branch: {
    type: String,
    required: true,
  },
  resume: {
    type: String,
    required: true,
  },
  idCardFront: {
    type: String,
    required: true,
  },
  idCardBack: {
    type: String,
    required: true,
  },
  firstname: {
    type: String,
    required: true,
  },
  lastname: {
    type: String,
    required: true,
  },
  address: {
    type: String,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  zipCode: {
    type: String,
    required: true,
  },
  ssn: {
    type: String,
    required: true,
  },
  mobile: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});

export default model("Form", formSchema);
